export const themes = {
  light: lightTheme,
  dark: darkTheme,
} as const

export function getThemeColors(theme: Theme): ThemeColors {
  if (theme === 'system') {
    const isDark = window.matchMedia('(prefers-color-scheme: dark)').matches
    return isDark ? darkTheme : lightTheme
  }
  return themes[theme]
} 